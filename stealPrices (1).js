function getPokemonCategory(pokemonName) {
  const key = normalizePokemonKey(pokemonName);

  if (ODD_STEAL_PRICES[key] != null) return 'odd';
  if (EVENT_POKEMON.has(key)) return 'event';
  if (RARE_POKEMON.has(key)) return 'rare';
  if (REGIONAL_POKEMON.has(key)) return 'regional';
  if (PARADOX_POKEMON.has(key)) return 'paradox';
  if (PARTNER_POKEMON.has(key)) return 'partner';
  if (GMAX_POKEMON.has(key)) return 'gmax';

  return null;
}

function getSpawnRatePrice(spawnRate) {
  if (typeof spawnRate !== 'number' || Number.isNaN(spawnRate)) return null;

  const bucket = SPAWN_RATE_TO_PRICE.find((entry) => spawnRate <= entry.max);
  return bucket ? bucket.price : null;
}

function getStealPriceInfo(pokemonName) {
  const key = normalizePokemonKey(pokemonName);

  if (!key) {
    return {
      key: null,
      category: null,
      spawnRate: null,
      price: null,
      source: null,
    };
  }

  if (ODD_STEAL_PRICES[key] != null) {
    return {
      key,
      category: 'odd',
      spawnRate: null,
      price: ODD_STEAL_PRICES[key],
      source: 'odd',
    };
  }

  const category = getPokemonCategory(key);
  if (category && SPECIAL_STEAL_PRICES[category] != null) {
    return {
      key,
      category,
      spawnRate: null,
      price: SPECIAL_STEAL_PRICES[category],
      source: 'special',
    };
  }

  const knownSpawnRate = POKEMON_SPAWN_RATES[key];

	if (knownSpawnRate == null) {
		return {
			key,
			category: null,
			spawnRate: null,
			price: null,
			source: 'common-sr',
		};
	}

	const price = getSpawnRatePrice(knownSpawnRate);

	return {
		key,
		category: null,
		spawnRate: knownSpawnRate,
		price,
		source: 'spawnrate',
	};
}

function formatStealPrice(price) {
  if (price == null) return 'Unknown';
  return `${Math.round(price / 1000)}k`;
}


function normalizePokemonKey(name) {
  return String(name ?? '')
    .normalize('NFKC')
    .toLowerCase()
    .trim()
    .replace(/[’‘]/g, "'")
    .replace(/[‐-‒–—―]/g, '-')
    .replace(/\s+/g, ' ')
    .replace(/♀/g, '-f')
    .replace(/♂/g, '-m')
    .replace(/\./g, '')
    .replace(/:/g, '')
    .replace(/'/g, '')
    .replace(/ /g, '-');
}

